"""# 6. Write a Python script to perform the following operations on a singly linked list

a. Create a list

b. Find the smallest element from the list

C. Insert an element if it is not a duplicate element

d. Display the elements in reverse order

"""

class Node:
    def __init__(self, data):
        self.data = data
        self.next = None

class LinkedList:
    def __init__(self):
        self.head = None

    def insert(self, data):
        new_node = Node(data)
        if self.head is None:
            self.head = new_node
        else:
            current = self.head
            while current.next:
                current = current.next
            current.next = new_node

    def find_smallest(self):
        if self.head is None:
            return None
        current = self.head
        smallest = current.data
        while current:
            if current.data < smallest:
                smallest = current.data
            current = current.next
        return smallest

    def insert_unique(self, data):
        current = self.head
        while current:
            if current.data == data:
                print(f"{data} is a duplicate element and will not be inserted.")
                return
            current = current.next
        self.insert(data)

    def display_reverse(self):
        if self.head is None:
            print("The list is empty.")
            return
        stack = []
        current = self.head
        while current:
            stack.append(current.data)
            current = current.next
        while stack:
            print(stack.pop(), end=" -> ")
        print("None")

