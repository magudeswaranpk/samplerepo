def insertion_sort(input_list):
    for i in range(1, len(input_list)):
        current_element = input_list[i]
        j = i - 1

        while j >= 0 and current_element < input_list[j]:
            input_list[j + 1] = input_list[j]
            j -= 1

        input_list[j + 1] = current_element

